#!/bin/bash

echo Disable leds...
i2cset -y 2 0x55 0x00 0x01

echo Set value...
i2cset -y 2 0x55 0x02 0x00

echo Set off color...
i2cset -y 2 0x55 0x0B 0x10
i2cset -y 2 0x55 0x0C 0x0
i2cset -y 2 0x55 0x0D 0x0

echo Set on color...
i2cset -y 2 0x55 0x0E 0x00
i2cset -y 2 0x55 0x0F 0x20
i2cset -y 2 0x55 0x10 0x00

echo Use custom colors
i2cset -y 2 0x55 0x0A 0x01

echo Set brightness...
i2cset -y 2 0x55 0x03 0xFF

echo To progress mode...
i2cset -y 2 0x55 0x01 0x50

echo Enable leds...
i2cset -y 2 0x55 0x00 0x00

for i in {1..255}
do
	HEXVALUE=`printf '%x\n' $i`
	#echo 0x$HEXVALUE
	i2cset -y 2 0x55 0x02 0x$HEXVALUE
	sleep 0.05
done
